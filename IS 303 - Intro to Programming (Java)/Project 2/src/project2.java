    /**
     * calcGPA()
     * 
     * @description Converts a letter grade and number of credits into a weighted score
     *              from form inputs.
     * 
     * @param grade : Combo box containing a letter grade between A and E
     * @param numCredits : Text field containing a number value
     * @return Weighted grade as double.
     */
    private double calcGPA(javax.swing.JComboBox grade, javax.swing.JTextField numCredits)
    {
        String letterGrade = String.valueOf(grade.getSelectedItem());
        double credits = Double.parseDouble(numCredits.getText());
        
        double gradePoints = convertLetterGrade(letterGrade);
        
        return gradePoints * credits;
    }
    
    /**
     * convertLetterGrade()
     * 
     * @description Converts a letter grade to its corresponding value on a 4
     *              point scale
     * 
     * @param grade : Letter grade between A and E to be converted
     * @return Grade points on a 4.0 scale as a double
     */
    private double convertLetterGrade(String grade)
    {
        double gradePoint;
        
        switch (grade)
        {
            case "A"  : gradePoint = 4.0; break;
            case "A-" : gradePoint = 3.7; break;
            case "B+" : gradePoint = 3.4; break;
            case "B"  : gradePoint = 3.0; break;
            case "B-" : gradePoint = 2.7; break;
            case "C+" : gradePoint = 2.4; break;
            case "C"  : gradePoint = 2.0; break;
            case "C-" : gradePoint = 1.7; break;
            case "D+" : gradePoint = 1.4; break;
            case "D"  : gradePoint = 1.0; break;
            case "D-" : gradePoint = 0.7; break;
            case "E"  : gradePoint = 0.0; break;
            default   : gradePoint = 0.0;
        }
        
        return gradePoint;
    }
    
