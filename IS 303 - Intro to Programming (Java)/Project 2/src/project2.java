    private void btnCalcGPAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCalcGPAMouseClicked
        
        // Get the weighted score for each course
        double is100Weighted = calcGPA( gradeIS100, creditsIS100 );
        double is102Weighted = calcGPA( gradeIS102, creditsIS102 );
        double is201Weighted = calcGPA( gradeIS201, creditsIS201 );
        double is303Weighted = calcGPA( gradeIS303, creditsIS303 );
        double acc200Weighted = calcGPA( gradeACC200, creditsACC200 );
        double fin201Weighted = calcGPA( gradeFIN201, creditsFIN201 );
        double busm241Weighted = calcGPA( gradeBUSM241, creditsBUSM241 );
        
        // Calculate the total number of credits
        double totalCredits = Double.parseDouble(creditsIS100.getText()) +
                                Double.parseDouble(creditsIS102.getText()) +
                                Double.parseDouble(creditsIS201.getText()) +
                                Double.parseDouble(creditsIS303.getText()) +
                                Double.parseDouble(creditsACC200.getText()) +
                                Double.parseDouble(creditsFIN201.getText()) +
                                Double.parseDouble(creditsBUSM241.getText());
        
        // Add up all the weighted averages
        double totalGradePoints = is100Weighted + is102Weighted + is201Weighted
                + is303Weighted + acc200Weighted + fin201Weighted + busm241Weighted;
        
        // Calculate the GPA
        double GPA = totalGradePoints / totalCredits;
        
        // Set the text of the GPA label (Format X.XX)
        lblGPA.setText(decimalFormat.format(GPA));
        
        
        // Control which "stoplight" appears
        if ( GPA >= 3.7 ) {
            pnlGreen.setVisible(true);
            pnlYellow.setVisible(false);
            pnlRed.setVisible(false);
        } else if ( GPA >= 3.2 ) {
            pnlGreen.setVisible(false);
            pnlYellow.setVisible(true);
            pnlRed.setVisible(false);
        } else {
            pnlGreen.setVisible(false);
            pnlYellow.setVisible(false);
            pnlRed.setVisible(true);
        }
    }//GEN-LAST:event_btnCalcGPAMouseClicked

